package com.libreria.techbook.model;

public class Moderatore {

    private int id;
    private String userMod;
	private String titoloMod;
	private String genereMod;
	private String autoreMod;
	private String copertinaMod;


    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getUserMod() {
        return userMod;
    }
    public void setUserMod(String userMod) {
        this.userMod = userMod;
    }
    public String getTitoloMod() {
        return titoloMod;
    }
    public void setTitoloMod(String titoloMod) {
        this.titoloMod = titoloMod;
    }
    public String getGenereMod() {
        return genereMod;
    }
    public void setGenereMod(String genereMod) {
        this.genereMod = genereMod;
    }
    public String getAutoreMod() {
        return autoreMod;
    }
    public void setAutoreMod(String autoreMod) {
        this.autoreMod = autoreMod;
    }
    
    public String getCopertinaMod() {
        return copertinaMod;
    }
    public void setCopertinaMod(String copertinaMod) {
        this.copertinaMod = copertinaMod;
    }

    

}
